window.onload = function () {
    var win = document.getElementById("jd_win");
    win.style.display = "none";
}

function show() {
    var win = document.getElementById("jd_win");
    win.style.display = "block";
}
function hidden() {
    var win = document.getElementById("jd_win");
    win.style.display = "none";
}